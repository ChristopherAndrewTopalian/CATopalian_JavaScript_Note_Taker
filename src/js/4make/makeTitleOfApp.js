function makeTitleOfApp()
{
    let titleOfApplication = ce("a");
    titleOfApplication.style.fontSize = '17px';
    titleOfApplication.style.color = 'rgb(170, 170, 170)';
    titleOfApplication.style.fontWeight = 'bold';
    titleOfApplication.href = 'https://github.com/ChristopherAndrewTopalian/CATopalian_JavaScript_Note_Taker'
    titleOfApplication.target = '_blank';
    titleOfApplication.textContent = ' CATopalian JavaScript Note Taker';
    titleOfApplication.style.textDecoration = 'none';
    return titleOfApplication;
}

